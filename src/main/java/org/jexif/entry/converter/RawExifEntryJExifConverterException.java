package org.jexif.entry.converter;

public class RawExifEntryJExifConverterException extends JExifConverterException {
    public RawExifEntryJExifConverterException() {
    }

    public RawExifEntryJExifConverterException(String message) {
        super(message);
    }

    public RawExifEntryJExifConverterException(String message, Throwable cause) {
        super(message, cause);
    }

    public RawExifEntryJExifConverterException(Throwable cause) {
        super(cause);
    }

    public RawExifEntryJExifConverterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
